let calcularMediaNotas = (nota1, nota2, nota3) => {
    // Calculando a média
    const media = (nota1 + nota2 + nota3) / 2;
    return media;
}

let calcularSomaNotas = (nota1, nota2,nota3) => {
    // Calculando a média
    const media = (nota1 + nota2 + nota3);
    return media;
}

module.exports =  {
    calcularMediaNotas,
    calcularSomaNotas
}

